package aplicacion;

public class Plataforma {
	private int[] puntoUno;
	private int[] puntoDos;
	
	
	public Plataforma(int[] puntoUno, int[] puntoDos) {
		this.puntoUno = puntoUno;
		this.puntoDos = puntoDos;
	}
	
	public int[] getPuntoUno() {
		return puntoUno;
	}
	
	public int[] getPuntoDos() {
		return puntoDos;
	}
	

	
	


}
